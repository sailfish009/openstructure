void FillData(ost::seq::alg::SubstWeightMatrix* subst, short (&data)[23][23]){
  char chars[23] = {'A','B','C','D','E','F','G','H','I','K','L','M','N','P','Q',
                    'R','S','T','V','W','X','Y','Z'};
  for(uint i = 0; i < 23; ++i){
    for(uint j = 0; j < 23; ++j){
      subst->SetWeight(chars[i],chars[j],data[i][j]);
    }
  }
}

